using System;
using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class SupplyTide
{
    public static string InvestAtFir(double a)
    {
        return Math.Round(a, LethalSoviet.ClimbGrower).ToString();
    }

    public static double Climb(double a)
    {
        return Math.Round(a, LethalSoviet.ClimbGrower);
    }
}
